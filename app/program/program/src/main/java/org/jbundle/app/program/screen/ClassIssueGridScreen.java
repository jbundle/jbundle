/**
 *  ClassIssueGridScreen - .
 */
public class ClassIssueGridScreen extends IssueGridScreen
{
    /**
     * Default constructor.
     */
    public ClassIssueGridScreen()
    {
        super();
    }
    /**
     * Constructor.
     * @param record The main record for this screen.
     * @param itsLocation The location of this component within the parent.
     * @param parentScreen The parent screen.
     * @param fieldConverter The field this screen field is linked to.
     * @param iDisplayFieldDesc Do I display the field desc?.
     */
    public ClassIssueGridScreen(Record record, ScreenLocation itsLocation, BasePanel parentScreen, Converter fieldConverter, int iDisplayFieldDesc, Map<String,Object> properties)
    {
        this();
        this.init(record, itsLocation, parentScreen, fieldConverter, iDisplayFieldDesc, properties);
    }
    /**
     * Initialize class fields.
     */
    public void init(Record record, ScreenLocation itsLocation, BasePanel parentScreen, Converter fieldConverter, int iDisplayFieldDesc, Map<String,Object> properties)
    {
        super.init(record, itsLocation, parentScreen, fieldConverter, iDisplayFieldDesc, properties);
    }
    /**
     * Add all the screen listeners.
     */
    public void addListeners()
    {
        super.addListeners();
        Record record = this.getMainRecord();
        Record recClassInfo = this.getRecord(ClassInfo.CLASS_INFO_FILE);
        if (recClassInfo != null)
        {
            record.setKeyArea(Issue.CLASS_INFO_ID_KEY);
            SubFileFilter listener = new SubFileFilter(recClassInfo.getField(ClassInfo.kID), Issue.CLASS_INFO_ID, null, null, null, null, true);
            record.addListener(listener);
            recClassInfo.getField(ClassInfo.kID).addListener(new FieldReSelectHandler(this));
        }
    }

}
