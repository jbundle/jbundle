/**
 *  RunProcessInField - Where to run this process:
1 - Locally
2 - Locally as a process
3 - Remotely.
 */
public class RunProcessInField extends StringPopupField
{
    public static final String LOCAL = "L";
    public static final String LOCAL_PROCESS = "P";
    public static final String REMOTE_PROCESS = "R";
    /**
     * Default constructor.
     */
    public RunProcessInField()
    {
        super();
    }
    /**
     * Constructor.
     * @param record The parent record.
     * @param strName The field name.
     * @param iDataLength The maximum string length (pass -1 for default).
     * @param strDesc The string description (usually pass null, to use the resource file desc).
     * @param strDefault The default value (if object, this value is the default value, if string, the string is the default).
     */
    public RunProcessInField(Record record, String strName, int iDataLength, String strDesc, Object strDefault)
    {
        this();
        this.init(record, strName, iDataLength, strDesc, strDefault);
    }
    /**
     * Constructor.
     * @param record The parent record.
     * @param strName The field name.
     * @param iDataLength The maximum string length (pass -1 for default).
     * @param strDesc The string description (usually pass null, to use the resource file desc).
     * @param strDefault The default value (if object, this value is the default value, if string, the string is the default).
     */
    public void init(Record record, String strName, int iDataLength, String strDesc, Object strDefault)
    {
        super.init(record, strName, iDataLength, strDesc, strDefault);
        if (iDataLength == DBConstants.DEFAULT_FIELD_LENGTH)
            m_iMaxLength = 1;
    }
    /**
     * Get the default value.
     * @return The default value.
     */
    public Object getDefault()
    {
        Object objDefault = super.getDefault();
        if (objDefault == null)
            objDefault = RunProcessInField.REMOTE_PROCESS;
        return objDefault;
    }
    /**
     * Get the conversion Map.
     */
    public String[][] getPopupMap()
    {
        String string[][] = {
            {RunProcessInField.LOCAL, "Run locally"}, 
            {RunProcessInField.LOCAL_PROCESS, "Local process"}, 
            {RunProcessInField.REMOTE_PROCESS, "Remote process"}, 
        };
        return string;
    }

}
