package org.jbundle.model.util;

/**
 * Since android doesn't include awt, here are Colors as ints.
 * @author don
 *
 */
public interface Colors {

    public final static int NULL = 0xffffffff;  // No color
    public final static int WHITE = 0x00ffffff;
    public final static int LIGHT_GRAY = 0x00c0c0c0;
    public final static int GRAY = 0x00808080;
    public final static int DARK_GRAY = 0x00404040;
    public final static int BLACK = 0x00000000;
    public final static int RED = 0x00ff0000;
    public final static int PINK = 0x00ffafaf;
    public final static int ORANGE = 0x00ffc800;
    public final static int YELLOW = 0x00ffff00;
    public final static int GREEN = 0x0000ff00;
    public final static int MAGENTA = 0x00ff00ff;
    public final static int CYAN = 0x0000ffff;
    public final static int BLUE = 0x000000ff;
    
}
